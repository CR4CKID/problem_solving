# \n = 줄바꿈
print("백문이 불여일견 \n백견이 불여일타")
# 백문이 불여일견
# 백견이 불여일타

# \", \' = 문장 내에서의 따옴표
print("저는 \"김대희\"입니다.") # 저는 "김대희"입니다.
print("저는 \'김대희\'입니다.") # 저는 '김대희'입니다.

# \\ = 문장 내에서의 \
print("C:\\Users\\CRACKID\\Desktop\\PythonWorkspace>") # C:\Users\CRACKID\Desktop\PythonWorkspace>

# \r = 문자열 앞부터 교체, 공백 포함
print("Red Apple\rPine") # PinaApple

# \b = 백스페이스
print("Redd\bApple") # RedApple

# \t = 탭
print("Red\tApple") # Red     Apple